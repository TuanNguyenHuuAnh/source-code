
! function(e) {
    "use strict";
    e.fn.formatEn = function() {
    	return this.is("input:text") == false ? this :
	    	this.on({
		    	 "keyup": function() {
	   			 var result = removeAccent($(this).val());
	   			 $(this).val(result);
	   		     return this;
		    	 }
		    }).each(function(key, value) {
				 var result = removeAccent($(this).val());
				 $(this).val(result);
			     return this;
	       });
    };
    
}(jQuery);

// document ready function
$(function() {
	// Format with class j-fm-en
	$('.j-fm-en').formatEn();
	
	// Format Numner by class j-fm-no
	$('.j-fm-no').number(true);

	// Format Numner decimal .00 by class j-fm-no-dec
	$('.j-fm-no-dec').number(true, 2);

});

/**
 * Xóa dấu tiếng việt
 * @param str
 * @returns
 */
function removeAccent(str) {
    str = str.replace(/à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ/g, "a");
    str = str.replace(/è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ/g, "e");
    str = str.replace(/ì|í|ị|ỉ|ĩ/g, "i");
    str = str.replace(/ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ/g, "o");
    str = str.replace(/ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ/g, "u");
    str = str.replace(/ỳ|ý|ỵ|ỷ|ỹ/g, "y");
    str = str.replace(/đ/g, "d");
    str = str.replace(/À|Á|Ạ|Ả|Ã|Â|Ầ|Ấ|Ậ|Ẩ|Ẫ|Ă|Ằ|Ắ|Ặ|Ẳ|Ẵ/g, "A");
    str = str.replace(/È|É|Ẹ|Ẻ|Ẽ|Ê|Ề|Ế|Ệ|Ể|Ễ/g, "E");
    str = str.replace(/Ì|Í|Ị|Ỉ|Ĩ/g, "I");
    str = str.replace(/Ò|Ó|Ọ|Ỏ|Õ|Ô|Ồ|Ố|Ộ|Ổ|Ỗ|Ơ|Ờ|Ớ|Ợ|Ở|Ỡ/g, "O");
    str = str.replace(/Ù|Ú|Ụ|Ủ|Ũ|Ư|Ừ|Ứ|Ự|Ử|Ữ/g, "U");
    str = str.replace(/Ỳ|Ý|Ỵ|Ỷ|Ỹ/g, "Y");
    str = str.replace(/Đ/g, "D");
    return str;
}